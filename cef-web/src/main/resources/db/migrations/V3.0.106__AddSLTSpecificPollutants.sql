CREATE TABLE IF NOT EXISTS pollutant_program_system_code
(
    id bigserial,
    pollutant_code VARCHAR(12),
    program_system_code  VARCHAR(20),
    CONSTRAINT pollutant_program_system_code_pkey PRIMARY KEY (id),
    CONSTRAINT pollutant_program_system_code_pol_fkey FOREIGN KEY (pollutant_code)
        REFERENCES pollutant (pollutant_code) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT pollutant_program_system_code_psc_fkey FOREIGN KEY (program_system_code)
        REFERENCES program_system_code (code) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
);

